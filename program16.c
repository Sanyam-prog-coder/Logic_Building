bool checkEvenOdd(int iNo)
{
    int iRem =0;

    iRem = iNo % 2;

    if(iRem == 0)
    { return true; }
    else
    { return false; }
}
////////////////////////////////////////////////////////////////////////////////
//
// Entry point function of the Description
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    bool bRet = false;

    printf("Enter number :");
    scanf("%d",&iValue);

    bRet = checkEvenOdd(iValue);

    if(bRet == true)
    { printf("%d is Even number\n",iValue);}    
    else
    { printf("%d is Odd number\n",iValue);}
    return 0;
}
